function returnPickIndex(round, second, team) {
  var passed = false;
  for (let i = 0; i < draftOrder[round - 1].length; i++) {
    if (draftOrder[round - 1][i] === team) {
      if ((second === false && passed === false) || (second === true && passed === true)) {
        return [(round - 1), i];
      } else {
        passed = true;
        continue;
      }
    }
  }
}

